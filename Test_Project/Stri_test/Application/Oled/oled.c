void leftShiftCharArray(char ch[CH_ROWS][CH_COLS]) {
    int row, col;
    char temp[CH_COLS];

    for (row = 0; row < CH_ROWS; row++) {
        // Save the first character in the row
        temp[0] = ch[row][0];

        // Copy the rest of the characters in the row to the temporary array
        for (col = 1; col < CH_COLS; col++) {
            temp[col] = ch[row][col];
        }

        // Shift all characters in the temporary array to the left by one position
        for (col = 0; col < CH_COLS - 1; col++) {
            ch[row][col] = temp[col + 1];
        }

        // Set the last character in the row to the saved character
        ch[row][CH_COLS - 1] = temp[0];
    }
}
