static void OLED_IIC_Wait_Ack()
{

	uint8_t ucErrTime=0;
	SDA_IN();      //SDA����Ϊ����  
	IIC_SDA=1;delay_us(1);	   
	IIC_SCL=1;delay_us(1);	 
	while(READ_SDA)
	{
		ucErrTime++;
		if(ucErrTime>250)
		{
			OLED_IIC_Stop();
			return ;
		}
	}
	IIC_SCL=0;//ʱ�����0 	   
	return ;  
}
